#ENTRADA DE DATOS 

radio = float(input("Digita el valor del radio:"))
PI = 3.1416

#PROCESOS (Cálculos u operaciones Matematicas y la logica)

área = (PI * radio ** 2)
perímetro = (2 * PI * radio)

#SALIDA DE DATOS

print("El AREA del circulo es igual a=", área)
print("El PERIMETRO del circulo es igual a=", perímetro)
